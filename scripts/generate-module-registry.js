#!/usr/bin/env node
/**
 * Scans lib/content/ for module files and generates lib/content/registry.ts
 * with auto-discovered imports.
 *
 * Usage: node scripts/generate-module-registry.js
 *
 * To add a new module:
 * 1. Create a .ts file in lib/content/<category>/ (e.g. lib/content/math/mein-modul.ts)
 * 2. Export a `module` object of type Module
 * 3. Run this script to regenerate the registry
 */

const fs = require("fs");
const path = require("path");

const CONTENT_DIR = path.join(__dirname, "..", "lib", "content");
const OUTPUT_FILE = path.join(CONTENT_DIR, "registry.ts");

function scanContent() {
  const modules = [];

  if (!fs.existsSync(CONTENT_DIR)) {
    return modules;
  }

  const categories = fs.readdirSync(CONTENT_DIR).filter((f) => {
    const stat = fs.statSync(path.join(CONTENT_DIR, f));
    return stat.isDirectory();
  });

  for (const category of categories) {
    const categoryDir = path.join(CONTENT_DIR, category);
    const files = fs.readdirSync(categoryDir).filter((f) => {
      return f.endsWith(".ts") && f !== "index.ts" && !f.startsWith(".");
    });

    for (const file of files) {
      const filename = file.replace(".ts", "");
      const varName = `${category}_${filename}`.replace(/[^a-zA-Z0-9_]/g, "_");
      modules.push({ category, filename, varName });
    }
  }

  return modules.sort(
    (a, b) => a.category.localeCompare(b.category) || a.filename.localeCompare(b.filename)
  );
}

function generateRegistry(modules) {
  const lines = [
    "// AUTO-GENERATED by scripts/generate-module-registry.js",
    "// Do not edit manually. Run: node scripts/generate-module-registry.js",
    "",
    'import { Module } from "../types";',
    "",
  ];

  for (const mod of modules) {
    lines.push(
      `import { module as ${mod.varName} } from "./${mod.category}/${mod.filename}";`
    );
  }

  lines.push("");
  lines.push("export const contentModules: Module[] = [");
  for (const mod of modules) {
    lines.push(`  ${mod.varName},`);
  }
  lines.push("];");
  lines.push("");

  return lines.join("\n");
}

const modules = scanContent();
const registry = generateRegistry(modules);

fs.writeFileSync(OUTPUT_FILE, registry, "utf-8");
console.log(`Generated ${OUTPUT_FILE} with ${modules.length} module(s)`);

if (modules.length === 0) {
  console.log("");
  console.log("No modules found in lib/content/. Add .ts files to get started.");
  console.log("");
  console.log("Example module file (lib/content/math/mein-modul.ts):");
  console.log('---');
  console.log('import { Module } from "../../types";');
  console.log("");
  console.log("export const module: Module = {");
  console.log('  id: "m-mein-modul",');
  console.log('  slug: "mathe-mein-modul",');
  console.log('  title: "Mein Modul",');
  console.log('  description: "Beschreibung",');
  console.log('  icon: "📐",');
  console.log('  color: "#8b5cf6",');
  console.log('  category: "grundlagen",');
  console.log("  progress: 0,");
  console.log("  lessons: [");
  console.log("    {");
  console.log('      id: "mm-1",');
  console.log('      title: "Lektion 1",');
  console.log('      duration: "10 min",');
  console.log('      type: "text",');
  console.log('      content: "Inhalt...",');
  console.log("    },");
  console.log("  ],");
  console.log("};");
  console.log('---');
  console.log("");
  console.log("Then run: node scripts/generate-module-registry.js");
}
